    private void btnagregarbebidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarbebidaActionPerformed
        String nombre = txtNombre.getText();
        String precio = txtprecio.getText();
        String contenido = txtContenido.getText();
        String marca = txtMarca.getText();
        String nivelazucar = txtNivelAzucar.getText();
        String descuento = "";
        if (rbtPromocionSi.isSelected()) {
            descuento = "si";
        } else if (rbtPromocionNo.isSelected()) {
            descuento = "no";
        }
        try {
            double precioD = Double.parseDouble(precio);
            double contenidoD = Double.parseDouble(contenido);
            double nivel = Double.parseDouble(nivelazucar);

            BebidasAzucaradas b1 = new BebidasAzucaradas(nombre,marca,contenidoD,precioD,nivel,descuento);
            almacen.agregarProducto(b1);
            rellenarTable();

        } catch (Exception ex) {

    }//GEN-LAST:event_btnagregarbebidaActionPerformed

    }

