    private void rellenarTable() {
        modelo.setRowCount(0);
        
        Productos b[][] = almacen.getEstanteria();
        Productos c=new BebidasAzucaradas();
        Productos bd[][]=almacen.getEstanteria();
        BebidasAzucaradas b1=new BebidasAzucaradas();
        Object[]fila;
        for (int i = 0; i < bd.length; i++) {
            for (int j = 0; j < bd[0].length; j++) {
                if (b[i][j] != null) {
                   fila=new Object[6];
                   fila[0]=bd[i][j].getNombre();
                   fila[1]=bd[i][j].getPrecio();
                   fila[2]=bd[i][j].getContenido();
                   fila[3]=bd[i][j].getMarca();
                   fila[4]=bd[i][j].getBebidas().getPorcentaje_azucar();
                   fila[5]=bd[i][j].getBebidas().getPromocion();
                   modelo.addRow(fila);
                }
            }
        }
    }

